// Returns the effective service window for a station.
// For interchange stations on multiple lines, takes the widest window
// (earliest open, latest close) so the station is available as long as
// any of its lines is running.
METRO.stationServiceHours=function(id){
  const s=METRO.STATIONS[id];
  if(!s) return{open:5,close:23};
  let open=24,close=0;
  s.l.forEach(line=>{
    const sh=METRO.SERVICE_HOURS[line];
    if(sh){open=Math.min(open,sh.open);close=Math.max(close,sh.close);}
  });
  return{open:open===24?5:open, close:close===0?23:close};
};

